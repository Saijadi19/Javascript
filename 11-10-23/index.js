document.write("Even numbers using loops and control statement.<br>")
i=2;
do{ 
 
{
    if(i%2==0){
        document.write(i,"<br>")
    }
    i++;


}
}while(i<=20);


document.write("Odd numbers using loops and control statement.<br>")
for(var i=0;i<=20;i++){
    if(i%2!=0){
        document.write(i,"<br>")
    }


}